// Classes and Instance
class Mahasiswa {
  constructor(nama, nim) {
    this.nama = nama;
    this.nim = nim;
  }

  print() {
    console.log(`Nama Mahasiswa : ${this.nama}`);
    console.log(`Nim Mahasiswa : ${this.nim}`);
  }
}

let Mahasiswa1 = new Mahasiswa("Andi", 2021061007);
let Mahasiswa2 = new Mahasiswa("Dhevan", 2021061003);

